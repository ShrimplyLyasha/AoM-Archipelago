# =============================================================================
# Age of Mythology Retold — Game-side bridge (XS state writer + log tail)
# =============================================================================
#
# Sister to ApClient.py.  Where ApClient.py owns the AP server connection and
# the slot_data, GameClient.py owns the bridge into the running game:
#
#   1. STATE EMISSION
#      `write_aom_state(ctx)` produces `<user_folder>/trigger/aom_state.xs`,
#      a giant XS source file that archipelago.xs `#include`s.  It declares
#      generated functions like `APInitItems()`, `APInitGods()`,
#      `APApplyMinorGodSelection()`, `APInitGodPowers()`, etc.  Each call
#      regenerates the file from the latest `received_items`, so the running
#      game sees a fresh state at the next scenario load.
#
#   2. AI ECHO TAIL
#      `read_new_checks(ctx)` parses `MythRetoldAIOutputPlayer12.txt` (where
#      AoMR's aiEcho output lands) for AP-prefixed tokens:
#         AP_CHECK:<id>      — game reports a location check fired
#         AP_LOCKED:<...>    — section is locked (display warning)
#         AP_SHOP:<slot_id>  — player bought a shop slot
#         AP_TRAP_FIRED:<id> — trap acknowledged so we can advance the queue
#         AP_RAND_GP:<...>   — random god power state report
#
#   3. PERSISTENCE
#      load_/save_ pairs persist `sent_checks`, shop / trap state, and a
#      session uuid into the per-game cache directory so an interrupted
#      session restores correctly on the next launch.
#
#   4. AI XS GENERATION
#      `generate_ap_ai_xs(ctx, mods_local_dir)` — overlays per-game data into
#      ap_ai_runtime.xs and copies it into the mod's AI directory so the
#      scenario can pick it up at next load.
#
# Key file paths (relative to the player's AoMR user folder):
#   * trigger/aom_state.xs          — generated by write_aom_state
#   * trigger/archipelago.xs        — bundled, includes aom_state.xs
#   * Game/AI/ap_ai_runtime.xs      — generated by generate_ap_ai_xs
#   * MythRetoldAIOutputPlayer12.txt — AI echo log we tail
#
# Adding a new generated state field:
#   1. Add a field to AoMGameContext (this file).
#   2. Read it in ApClient.py `_on_connected` from slot_data.
#   3. Emit corresponding XS into `write_aom_state` (often as a new
#      `APInitX()` function or an extension of an existing one).
#   4. Consume the generated function in archipelago.xs via APActivateScenario
#      or APApplyItems.
#
# Adding a new AP-prefixed token:
#   1. Make archipelago.xs print the token via `aiEcho()` from a Player 12
#      AI rule.
#   2. Add a parser branch in `read_new_checks`.
#   3. Forward the parsed event to AP-side via the `client_interface`
#      callback (typically `client_interface.send_msgs(...)`).
# =============================================================================

