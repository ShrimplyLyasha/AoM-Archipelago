def on_items_received(ctx: AoMGameContext, items: list[int]) -> None:
    """Hook fired by ApClient when the AP server sends a fresh ReceivedItems
    batch.  Replaces the entire received_items list (the server always sends
    the full set, not deltas) and regenerates aom_state.xs so the next
    scenario load picks up the new items.

    Args:
        items: the full list of item ids the server says we own (in the
               order they were received).  archipelago.xs reads these as
               `gAPItems[9..]` (after the 9 leading flag slots).
    """
    ctx.received_items = items
    write_aom_state(ctx)


